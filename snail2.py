def snail(snail_map):
    if len(snail_map[0]) > 1:
        final = []
        o = 0
        while snail_map:
            temp = []
            if o == 0:
                final.extend(first(snail_map))
                o += 1
            try:
                for i in range(len(snail_map)-1):
                    if i > 0 and i < len(snail_map)-1:
                        temp.append(snail_map[i][1:-1])
                final.extend(first(temp))
                snail_map = temp
            except IndexError:
                break
        return final
    else:
        return snail_map[0]


